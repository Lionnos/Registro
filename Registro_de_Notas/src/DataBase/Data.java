
package DataBase;

/**
 *
 * @author Henry
 */
public class Data {
    String Codigo ;
    String Nombre ;
    String Promedio ;

    public String getCodigo() {
        return Codigo;
    }

    public void setCodigo(String Codigos) {
        this.Codigo = Codigos;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombres) {
        this.Nombre = Nombres;
    }

    public String getPromedio() {
        return Promedio;
    }

    public void setPromedio(String Promedios) {
        this.Promedio = Promedios;
    }

   
    
}
